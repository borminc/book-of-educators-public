     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $subjects = Subject::query()
            ->when($search = request('search'), fn ($q) => $q->whereRaw('name LIKE ? OR keywords LIKE ?', ["%$search%", "%$search%"]))
            ->when($limit = request('limit'), fn ($q) => $q->limit($limit))
            ->get();

        return $subjects;
    }

